const { aiAnswers, aiIntents, qLegacyAlias, qLegacyArabicAlias, qConceptTerms } = window.MARIAM_AI_CONFIG;
const aiEngine = window.MARIAM_AI_ENGINE;
const hasArabic = text => /[\u0600-\u06FF]/.test(text);
const storedAiMemory = (() => {
  try { return JSON.parse(sessionStorage.getItem("mariamAiMemory") || "{}"); } catch { return {}; }
})();
const aiMemory = aiEngine.createMemory(storedAiMemory);
function saveAiMemory() {
  try { sessionStorage.setItem("mariamAiMemory", JSON.stringify(aiMemory)); } catch { /* Session memory remains available in-page. */ }
}
function getAiResult(question) {
  const result = aiEngine.classifyWithMemory(question, { aiIntents, qLegacyAlias, qLegacyArabicAlias, qConceptTerms }, aiMemory);
  saveAiMemory();
  return result;
}
function answerAi(question, topic) {
  const responseLanguage = hasArabic(question) ? "ar" : "en";
  if (topic) return aiAnswers[responseLanguage][topic];
  const result = getAiResult(question);
  const topics = result.topics?.length ? result.topics : [result.topic];
  return topics.map(item => aiAnswers[responseLanguage][item]).filter(Boolean).join("\n\n");
}
let aiTypingTimer;
function showAiAnswer(answer) {
  const response = document.getElementById("aiResponse");
  window.clearInterval(aiTypingTimer);
  response.style.opacity = 0;
  setTimeout(() => {
    response.textContent = "";
    response.style.opacity = 1;
    if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
      response.textContent = answer;
      return;
    }
    const startedAt = performance.now();
    aiTypingTimer = window.setInterval(() => {
      const progress = Math.min(1, (performance.now() - startedAt) / 760);
      const index = Math.ceil(answer.length * progress);
      response.textContent = answer.slice(0, index);
      if (progress >= 1) {
        response.textContent = answer;
        window.clearInterval(aiTypingTimer);
      }
    }, 32);
  }, 140);
}
