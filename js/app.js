let aiTypingTimer;
function showAiAnswer(answer) {
  const response = document.getElementById("aiResponse");
  window.clearInterval(aiTypingTimer);
  response.style.opacity = 0;
  setTimeout(() => {
    response.textContent = "";
    response.style.opacity = 1;
    if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
      response.textContent = answer;
      return;
    }
    const startedAt = performance.now();
    aiTypingTimer = window.setInterval(() => {
      const progress = Math.min(1, (performance.now() - startedAt) / 760);
      const index = Math.ceil(answer.length * progress);
      response.textContent = answer.slice(0, index);
      if (progress >= 1) {
        response.textContent = answer;
        window.clearInterval(aiTypingTimer);
      }
    }, 32);
  }, 140);
}
function renderAiSuggestions(suggestions = []) {
  const list = document.querySelector(".chip-list");
  list.innerHTML = "";
  suggestions.forEach(text => {
    const button = document.createElement("button");
    button.className = "question";
    button.type = "button";
    button.textContent = `✨ ${text}`;
    button.addEventListener("click", () => askMariamAi(text));
    list.appendChild(button);
  });
}
function askMariamAi(question) {
  const typing = document.getElementById("aiTyping");
  typing.hidden = false;
  const result = aiConversation.ask(question, aiSession);
  saveAiMemory();
  window.setTimeout(() => {
    typing.hidden = true;
    showAiAnswer(result.answer);
    renderAiSuggestions(result.suggestions);
    if (voiceReplyEnabled && aiVoice.isSynthesisSupported()) {
      pauseJourneyAudioForVoice();
      document.getElementById("stopVoiceReply").hidden = false;
      aiVoice.speak(result.answer, {
        lang: result.language,
        onEnd: () => { document.getElementById("stopVoiceReply").hidden = true; },
        onError: () => { document.getElementById("stopVoiceReply").hidden = true; }
      });
    }
  }, 260);
}
